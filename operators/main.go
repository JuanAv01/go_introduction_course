package main

import "fmt"

func main() {
	//
	yearOld := 24

	fmt.Println("Operators: ")
	fmt.Println(yearOld > 18) // true
	fmt.Println(yearOld < 18) // false
	fmt.Println(yearOld >= 18) // true
	fmt.Println(yearOld <= 18) // false
	fmt.Println(yearOld == 18)  // false
	fmt.Println(yearOld != 18)  // true
}